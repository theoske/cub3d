#include "./libft.h"

int	ft_isspace(char car)
{
	if (!car)
		return (0);
	if (car == ' ' || car == '\t' || car == '\n'
		|| car == '\v' || car == '\f' || car == '\r')
		return (1);
	return (0);
}
